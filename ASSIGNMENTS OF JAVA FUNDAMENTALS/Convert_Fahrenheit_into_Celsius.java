import java.util.*;
	public class Convert_Fahrenheit_into_Celsius {
		public static void main(String[] args) {
			Scanner sc = new Scanner(System.in);
			float F = sc.nextFloat();
		      System.out.print(" ");
		      float C;
			C = ((F - 32)*5)/9;
			System.out.print("Convert Fahrenheit into Celsius  ");
			System.out.print(C);
			
			
		}
}
