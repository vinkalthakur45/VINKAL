public class SumofthreeASCIIValues {
	public static void main(String args[]) {
		int a = 'a';
		System.out.println("a value is " + a);
		int a1 = 'D';
		System.out.println("D value is " + a1);
		int a2 = 'A';
		System.out.println("A value is " + a2);
		int result = a + a1 + a2;
		System.out.println("Sum of three characters value =" + result);

	}
}